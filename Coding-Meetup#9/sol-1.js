function isAgeDiverse(list) {
    // thank you for checking out the Coding Meetup kata :)
    return Array(10).fill(0).map((_, index) => index + 1).every((age) => {
        if (age === 10) {
            return list.find((dev) => dev.age > 100)
        } else {
            let min = age * 10
            let max = min + 9
            return list.find(dev => dev.age >= min && dev.age <= max)
        }
    })
}

